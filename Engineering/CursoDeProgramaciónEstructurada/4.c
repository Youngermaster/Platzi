// Variables y tipos de datos
#include <stdio.h>

int main()
{
    printf("Variables and data types \n\n");

    int aInteger = 11223;
    float aFloat = 1.15;
    double aDouble = 1.18;
    char aChar = 'S';

    printf("aInteger value is: %i \n", aInteger);
    printf("aFloat value is: %f \n", aFloat);
    printf("aDouble value is: %f \n", aDouble);
    printf("aChar value is: %c \n", aChar);

    return 0;
}