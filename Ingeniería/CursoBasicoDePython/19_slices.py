my_name = "Juan Manuel Young Hoyos"
print(my_name[0])

# * Both of them are equal
print(my_name[0:3])
print(my_name[:3])

# * Both of them are equal
print(my_name[1:len(my_name)])
print(my_name[1:])

# * Slicing with steps
print(my_name[1:len(my_name):2])

# * It brings me everything
print(my_name[::])
